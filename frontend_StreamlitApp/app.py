    st.title("📝 Live Text Analyzer")
    
    if not models_loaded:
        st.error("⚠️ Model files missing! Please add 'best_model.pkl' and 'best_vectorizer.pkl' to the directory.")
    
    user_input = st.text_area("Enter a movie review:", height=150, placeholder="e.g., 'The acting was brilliant but the plot was terrible...'")
    
    if st.button("Analyze Sentiment"):
        if user_input and models_loaded:
            # 1. Clean & Predict
            cleaned_input = clean_text(user_input)
            text_vector = vectorizer.transform([cleaned_input])
            prediction = model.predict(text_vector)[0]
            
            # 2. Confidence Score (SVM decision function to percentage)
            decision_score = model.decision_function(text_vector)[0]
            confidence = (abs(decision_score) / (abs(decision_score) + 1.5)) * 100
            if confidence > 99: confidence = 99.9  # Cap for realism
            
            # Display Results
            st.success("✅ Analysis Complete!")
            col1, col2 = st.columns(2)
            with col1:
                if prediction == "positive":
                    st.metric(label="Prediction", value="Positive 🍿")
                else:
                    st.metric(label="Prediction", value="Negative 👎")
            with col2:
                st.metric(label="Confidence Score", value=f"{confidence:.2f}%")
                
            # 3. Display which words influenced the prediction
            st.markdown("### 🔍 Words that influenced this prediction")
            
            # Extract weights from the SVM model
            feature_names = vectorizer.get_feature_names_out()
            coefs = model.coef_[0]
            
            words_in_input = set(cleaned_input.split())
            word_impact = []
            
            for w in words_in_input:
                if w in vectorizer.vocabulary_:
                    idx = vectorizer.vocabulary_[w]
                    word_impact.append((w, coefs[idx]))
            
            # Sort by impact
            word_impact.sort(key=lambda x: x[1], reverse=True)
            
            pos_words = [f"**{w}**" for w, score in word_impact if score > 0]
            neg_words = [f"**{w}**" for w, score in word_impact if score < 0]
            
            if pos_words:
                st.write("🟢 **Positive indicators:**", ", ".join(pos_words))
            if neg_words:
                st.write("🔴 **Negative indicators:**", ", ".join(neg_words))
                
